/*
Given an array A[ ] of positive integers of size N, where each value represents the number of chocolates in a packet. Each packet can have a variable number of chocolates. There are M students, the task is to distribute chocolate packets among M students such that :
1. Each student gets exactly one packet.
2. The difference between maximum number of chocolates given to a student and minimum number of chocolates given to a student is minimum.
*/
//{ Driver Code Starts
#include <bits/stdc++.h>
using namespace std;

// } Driver Code Ends
class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin(), a.end());

    // Initialize mini to maximum possible value
    long long mini = LLONG_MAX;

    // Iterate through the sorted array to find the minimum difference
    // j is initialized to m, as each student should get exactly one packet
    for(int i = 0; i < n && (i + m - 1) < n; i++) {
        long long currentDifference = abs(a[i] - a[i + m - 1]);

        // Update mini if the current difference is smaller
        mini = min(mini, currentDifference);
    }
    
    return mini;
    
    }   
};

